public class NerdPropertyException extends NerdException {
	
    public NerdPropertyException() {
        super();
    }

    public NerdPropertyException(String message) {
        super(message);
    }

    public NerdPropertyException(Throwable cause) {
        super(cause);
    }

    public NerdPropertyException(String message, Throwable cause) {
        super(message, cause);
    }
}
